<h2 id="what-is-orthographic-drawing" tabindex="-1"><a class="header-anchor" href="#what-is-orthographic-drawing"><span>What is Orthographic Drawing?</span></a></h2>
<p>Orthographic drawing is a technique used to represent 3D objects in 2D, using multiple views, typically top, front, and side projections. This method is commonly used in engineering, architecture, and design to show the exact dimensions and relationships of an object without distortion.</p>
<p>The main orthographic views—Top, Front, and Sides—allow viewers to understand the object’s shape, depth, and dimensions. (see image below)</p>
